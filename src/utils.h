void normalize_vectors(float* data, int num_vectors, int dim) {
    for (int i = 0; i < num_vectors; i++) {
        float norm = 0.0;
        for (int j = 0; j < dim; j++) {
            norm += data[i * dim + j] * data[i * dim + j];
        }
        norm = sqrt(norm);
        if (norm > 0) {
            for (int j = 0; j < dim; j++) {
                data[i * dim + j] /= norm;
            }
        }
    }
}

/* get dimension of the fvec/ivec file */
int xvec_get_dim(std::ifstream& file) {
    if(!file.is_open())
        throw std::runtime_error("Error running xvec_get_dim : file not open");

    // read 4 bytes to get dim
    int dim;
    file.read(reinterpret_cast<char*>(&dim), sizeof(std::uint32_t));

    // reset file position
    file.seekg(0, std::ios::beg);

    return dim;
}

/* import fvec/ivec file into array including file opening and closing */
template<typename datatype>
void xvecfile_to_arr(std::string datapath, std::string dataname, std::string filetype, datatype **outdata, int arrsize, int dim) {
    std::string path = datapath + dataname + "/" + filetype;
    std::ifstream datafile(path, std::ios::binary);

    if(!datafile.is_open())
        throw std::runtime_error("xvecfile_to_arr : Failed to open data file.\n");

    *outdata = new datatype[arrsize * dim];
    int d;
    for (int i = 0; i < arrsize; i++)
    {
        // read 4 bytes to get dim
        datafile.read(reinterpret_cast<char*>(&d), sizeof(std::uint32_t));
        if(dim != d)
            throw std::runtime_error("Error running xvec_to_arr : dims don't match");
            
        // read (dim * datatype size) bytes to get data
        datafile.read(reinterpret_cast<char*>(&(*outdata)[dim * i]), dim * sizeof(datatype));
    }

    datafile.close();
}

