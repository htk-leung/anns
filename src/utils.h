template<typename datatype>
bool arr_to_xvecfile(datatype *arr, uint32_t arrSize, uint32_t dim, std::ofstream& file, std::string filename) {

    if (!file.is_open())
        throw std::runtime_error(std::string("Failed to open or process file: ") + filename);
    
    for (int i = 0; i < arrSize; i++)
    {
        // Write size first
        file.write(reinterpret_cast<const char*>(&dim), sizeof(uint32_t));
        
        // Write data
        file.write(reinterpret_cast<const char*>(arr), dim * sizeof(datatype));
        arr += dim;
    }
    
    return true;
}

