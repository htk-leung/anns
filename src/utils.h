/* function to generate random data as an float array */
/* float only because data is usually represented as floats */
float *create_data(int dim, int max_elements) {
    #ifdef DEBUG
    std::cout<<"in func create_data()"<<std::endl;
    #endif
    
    std::random_device rd;
    std::mt19937 rng(rd());
    std::uniform_real_distribution<> distrib_real(-100.0, 100.0);

    float* data = new float[dim * max_elements];

    for (int i = 0; i < dim * max_elements; i++) {
        data[i] = distrib_real(rng);
    }

    return data;
}

float *find_qs(float *data, int dim, int numq) {
    int n = numq * dim;
    float *qs = new float[n];               // Generate space
    float *temp = qs;                       // Ptr for increment

    for (int i = 0; i < n; i++)             // For each query
        *temp++ = data[i];                  // Generate random data index
    
    return qs;
}

template<typename datatype>
float percentile(std::vector<datatype> v, int p, int qnum) {
    // check for valid p
    if (p < 0 || p > 100)
        throw std::invalid_argument("Usage : 0 <= p <= 100");

    // calculate tentative index
    float i = p * (qnum-1) / 100.0f;

    // floor, ceiling
    int low = static_cast<int>(std::floor(i));
    int high = static_cast<int>(std::ceil(i));

    // if floor = ceiling return either
    if (low == high)
        return v[low];
    // else return interpolation
    else {
        float w = i - low;
        return v[low] + (v[high] - v[low]) * w;
    }
}

template<typename datatype>
float average(std::vector<datatype> vec) {
    float n = static_cast<float>(vec.size());
    float sum = 0.0;

    for (auto v : vec)
        sum += v;

    return sum / n;
}