/* read data generated by create_data_rand.cpp into an array */
template<typename datatype>
datatype *bin_to_arr(std::ifstream& file, std::string filename, int arrsize) {

    if (!file.is_open()) 
        throw std::runtime_error(std::string("Failed to open or process file: ") + filename);
    
    // Read sizes first
    uint32_t dim;
    file.read(reinterpret_cast<char*>(&dim), sizeof(uint32_t));
    #ifdef DEBUG
    std::cout << "reading dataset of dimension " << dim << " and size " << arrsize << std::endl;
    #endif

    // Then read data with size dim * intSize * sizeof(-)
    datatype *arr = new datatype[dim * arrsize];
    file.read(reinterpret_cast<char*>(arr), dim * arrsize * sizeof(datatype));
    
    return arr;
}

/* write data to binary file */
template<typename datatype>
bool arr_to_bin(datatype *arr, uint32_t arrSize, uint32_t dim, std::ofstream& file, std::string filename) {

    if (!file.is_open())
        throw std::runtime_error(std::string("Failed to open or process file: ") + filename);
    
    // Write size first
    file.write(reinterpret_cast<const char*>(&dim), sizeof(uint32_t));
    
    // Write data
    file.write(reinterpret_cast<const char*>(arr), arrSize * dim * sizeof(datatype));
    
    return true;
}

