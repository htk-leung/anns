/* function to generate random data as an float array */
/* float only because data is usually represented as floats */
float *create_data(int dim, int max_elements) {
    #ifdef DEBUG
    std::cout<<"in func create_data()"<<std::endl;
    #endif
    
    std::random_device rd;
    std::mt19937 rng(rd());
    std::uniform_real_distribution<> distrib_real;

    float* data = new float[dim * max_elements];

    for (int i = 0; i < dim * max_elements; i++) {
        data[i] = distrib_real(rng);
    }

    return data;
}